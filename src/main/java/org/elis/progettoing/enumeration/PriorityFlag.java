package org.elis.progettoing.enumeration;

/**
 * Enumeration representing the priority levels of tickets.
 * <p>
 * The possible priority levels are:
 * <ul>
 *     <li><strong>LOW</strong> - Low priority.</li>
 *     <li><strong>MEDIUM</strong> - Medium priority.</li>
 *     <li><strong>HIGH</strong> - High priority.</li>
 * </ul>
 * </p>
 */
public enum PriorityFlag {
    LOW,

    MEDIUM,

    HIGH
}
