package org.elis.progettoing.enumeration;

/**
 * Enumeration representing the different statuses of an auction.
 * <p>
 * The possible states are:
 * <ul>
 *     <li><strong>PENDING</strong> - The auction is waiting to start.</li>
 *     <li><strong>OPEN</strong> - The auction is open and active.</li>
 *     <li><strong>CLOSED</strong> - The auction is closed.</li>
 * </ul>
 * </p>
 */
public enum AuctionStatus {
    PENDING,

    OPEN,

    CLOSED
}
